package br.com.impacta.programa;

public class VariaveisInteirasGerais {
	public static void main(String[] args) {
		int bDecimal = 100;
		int bOctal = 0100;
		int bHexadecimal = 0x100;
		int bBinaria = 0b100;
		
		System.out.println("Decimal: "+bDecimal);
		System.out.println("Octal: "+bOctal);
		System.out.println("Hexadecimal: "+bHexadecimal);
		System.out.println("Binaria: "+bBinaria);
	}
}
